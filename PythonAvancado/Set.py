# um tipo de lista que exclui as duplicatas
# não possui indexação, ou seja, não é possível acessar um item por meio do indice
# os itens devem ser postos entre chaves

cesta = {'banana', 'maçã','laranja','banana', 'maçã','laranja'}
print(cesta)
# terminal: {'banana', 'maçã', 'laranja'}

# criar uma lista dos caracteres de uma palavra
palavra = set('Palmeiras')
print(palavra)
# terminal: {'a', 'r', 'e', 'i', 'P', 'm', 's', 'l'}