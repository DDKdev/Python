# Divisão float 2.0 e Divisão inteiro 2
divisaoFloat = 10/5
divisaoInteiro = 10//5
print("Os diferentes resultados para as divisões: ",
      divisaoFloat, " e ", divisaoInteiro)

# mínimo, máximo, soma, potenciação
minimo = min(1, 2, 3, 4, 5)
somaTotal = sum([1, 2, 3])
p = pow(2, 3)
potenciacao = 2**3
print("Resultados: ", minimo, somaTotal, p, potenciacao)
